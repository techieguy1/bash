#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
# Define colors
#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
_colors() {
   # Escape characters
   ESC="\e["
   
   # Set
   BOLD="${ESC}1m"
   DIM="${ESC}2m"
   UNDERLINE="${ESC}4m"
   BLINK="${ESC}5m"
   REVERSE="${ESC}7m"
   HIDDEN="${ESC}8m"

   # Reset
   RESET_ALL="${ESC}0m"
   NO_BOLD="${ESC}21m"
   NO_DIM="${ESC}22m"
   NO_UNDERLINE="${ESC}24m"
   NO_BLINK="${ESC}25m"
   NO_REVERSE="${ESC}27m"
   NO_HIDDEN="${ESC}28m"
         
   # Standard 16 colors
   BLACK="${ESC}30m"
   RED="${ESC}31m"
   GREEN="${ESC}32m"
   ORANGE="${ESC}33m"
   BLUE="${ESC}34m"
   PURPLE="${ESC}35m"
   CYAN="${ESC}36m"
   LIGHTGRAY="${ESC}37m"
   DARKGRAY="${ESC}90m"
   LIGHTRED="${ESC}91m"
   LIGHTGREEN="${ESC}92m"
   YELLOW="${ESC}93m"
   LIGHTBLUE="${ESC}94m"
   LIGHTPURPLE="${ESC}95m"
   LIGHTCYAN="${ESC}96m"
   WHITE="${ESC}97m"
   
   # Backgrounds
   BACK_BLACK="${ESC}40m"
   BACK_RED="${ESC}41m"
   BACK_GREEN="${ESC}42m"
   BACK_ORANGE="${ESC}43m"
   BACK_BLUE="${ESC}44m"
   BACK_PURPLE="${ESC}45m"
   BACK_CYAN="${ESC}46m"
   BACK_LIGHTGRAY="${ESC}47m"
   BACK_DARKGRAY="${ESC}100m"
   BACK_LIGHTRED="${ESC}101m"
   BACK_LIGHTGREEN="${ESC}102m"
   BACK_YELLOW="${ESC}103m"
   BACK_LIGHTBLUE="${ESC}104m"
   BACK_LIGHTPURPLE="${ESC}105m"
   BACK_LIGHTCYAN="${ESC}106m"
   BACK_WHITE="${ESC}107m"
}

#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
# Line separator
#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
_separator() {
   printf "${CYAN}~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~${RESET_ALL}\n"
}

#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
# Error
#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
_error() {
   printf "${WHITE}${BACK_RED}!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!${RESET_ALL}\n"
   printf "${WHITE}${BACK_RED}!    E R R O R     E R R O R     E R R O R     E R R O R     E R R O R     E R R O R     E R R O R    !${RESET_ALL}\n"     
   printf "${WHITE}${BACK_RED}!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!${RESET_ALL}\n"
}

#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
# 
#                    M   A   I   N
#
#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
_colors
